# Interface/Contract Naming Conventions

All interfaces (contracts) MUST be located in `app/Contracts/` directory following these guidelines.

## Naming Rules

- **NO suffix** (e.g., `GeneratesAiResponse`, not `GeneratesAiResponseInterface`)
- Use descriptive, action-oriented names
- Examples:
  - `GeneratesAiResponse`
  - `ValidatesUserInput`
  - `ProcessesPayments`
